# -*- coding: utf-8 -*-

import unittest
from ddt import ddt, data, unpack

from SweepingRobot import *


@ddt
class TestSweepingRobot(unittest.TestCase):
    @data(
        ([[2, 2, 2]], (0, 1), (0, 1)),
        ([[2, 2]], (0, 0), (0, 1)),
        ([[2, 2, 2], [2, 2, 2], [2, 2, 2]], (2, 2), (-1, 0)),
        ([[2, 2, 2], [2, 0, 2], [2, 2, 2]], (0, 0), (0, 1)),
        ([[2, 2, 2], [2, 0, 0], [2, 2, 2]], (2, 0), (1, 0)),
        ([[2, 2, 2], [2, 0, 0], [2, 2, 2]], (2, 0), (-1, 0)),
        ([[2, 2, 2], [2, 0, 0], [2, 2, 2]], (2, 0), (0, 1)),
        ([[2, 2, 2], [2, 0, 0], [2, 2, 2]], (2, 0), (0, -1)),
        ([[0, 2, 0], [2, 2, 2], [0, 2, 0]], (1, 1), (0, -1)),
        ([[0, 2, 0], [2, 2, 2], [0, 2, 0]], (1, 0), (0, -1)),
        ([[2, 0, 0, 2, 2], [2, 2, 2, 2, 0], [0, 2, 0, 2, 2], [
         2, 0, 2, 2, 0], [2, 0, 2, 0, 2], [2, 2, 2, 2, 2]], (0, 0), (1, 0)),
        ([[2, 0, 0, 2, 2], [2, 2, 2, 2, 0], [0, 2, 0, 2, 2], [
         2, 0, 2, 2, 0], [2, 0, 2, 0, 2], [2, 2, 2, 2, 2]], (4, 4), (0, -1)),
    )
    # @unpack
    def test_SweepingRobot(self, testcase):
        self.assertEqual(True, sweep(*testcase))


if __name__ == '__main__':
    unittest.main(verbosity=1)
